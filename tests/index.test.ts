import { tanh } from 'tanh';
import { describe, expect, it } from 'vitest';

describe('tanh', () => {
  it('is a function', () => {
    expect(typeof tanh).toBe('function');
  });

  it('returns NaN for NaN', () => {
    expect(tanh(Number.NaN)).toBeNaN();
  });

  it('returns +0 for +0', () => {
    expect(tanh(+0)).toBe(+0);
  });

  it('returns -0 for -0', () => {
    expect(tanh(-0)).toBe(-0);
  });

  it('returns +1 for +Infinity', () => {
    expect(tanh(Number.POSITIVE_INFINITY)).toBe(+1);
  });

  it('returns -1 for -Infinity', () => {
    expect(tanh(Number.NEGATIVE_INFINITY)).toBe(-1);
  });
});

describe('algebraic expression', () => {
  const TOLERANCE = 0.5 * 10 ** -4;

  it('matches (exp(x) - exp(-x)) / (exp(x) + exp(-x))', () => {
    for (let i = -20; i < 20; i++) {
      const expected =
        (Math.exp(i) - Math.exp(-i)) / (Math.exp(i) + Math.exp(-i));
      expect(Math.abs(tanh(i) - expected)).toBeLessThan(TOLERANCE);
    }
  });
});

